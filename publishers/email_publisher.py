def _pick_card(pick):
    ht_cls = " ht" if pick.ht_model else ""
    ht_label = "🔶 HT MODEL — " if pick.ht_model else ""
    stats = f"""
    <div class="stats">
      <div class="stat">Edge <span>{pick.edge_pct:.1f}pp</span></div>
      <div class="stat">Fair <span>{pick.fair_odds:.2f}</span></div>
      <div class="stat">Market <span>{pick.market_odds:.2f}</span></div>
      <div class="stat">Value <span>+{pick.value_pct:.1f}%</span></div>
      {"<div class='stat'>Strength <span>" + f"{pick.strength:.0f}/100</span></div>" if pick.strength else ""}
      {"<div class='stat'>n <span>" + f"{pick.n_matches}</span></div>" if pick.n_matches else ""}
    </div>"""
    set_html = ""
    if pick.fair_2_0 or pick.fair_2_1 or pick.fair_win_1set:
        parts = []
        if pick.fair_2_0: parts.append(f"2-0: <b>{pick.fair_2_0:.2f}</b>")
        if pick.fair_2_1: parts.append(f"2-1: <b>{pick.fair_2_1:.2f}</b>")
        if pick.fair_win_1set: parts.append(f"Win 1+ set: <b>{pick.fair_win_1set:.2f}</b>")
        set_html = f'<div class="set-prices">Set prices: {" | ".join(parts)}</div>'
    return f"""
    <div class="pick-card{ht_cls}">
      <p class="pick-match">{pick.emoji} {pick.match}</p>
      <p class="pick-meta">{pick.league} · {pick.kickoff_str}</p>
      <p class="pick-bet{ht_cls}">✅ {ht_label}{pick.market} → {pick.selection}</p>
      {stats}{set_html}
    </div>"""

