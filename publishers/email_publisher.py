def send_weekly_digest(football_picks, tennis_picks, recipients=None):
    cfg = CONFIG.substack
    to_list = recipients or [cfg.substack_email]
    if not to_list or not to_list[0]:
        log.error("No recipient email configured")
        return False
    if CONFIG.dry_run:
        log.info(f"[DRY RUN] Weekly digest → {to_list} | fb={len(football_picks)} tn={len(tennis_picks)}")
        return True
    subject = f"📊 ValueEdge Weekly — {date.today().strftime('%d %b %Y')}"
    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"] = cfg.from_email
    msg["To"] = ", ".join(to_list)
    msg.attach(MIMEText(build_weekly_html(football_picks, tennis_picks), "html"))
    try:
        with smtplib.SMTP(cfg.smtp_host, cfg.smtp_port) as server:
            server.ehlo()
            server.starttls()
            server.login(cfg.smtp_user, cfg.smtp_pass)
            server.sendmail(cfg.from_email, to_list, msg.as_string())
        log.info(f"✅ Weekly digest sent to {len(to_list)} recipient(s)")
        return True
    except Exception as e:
        log.error(f"Email error: {e}")
        return False
